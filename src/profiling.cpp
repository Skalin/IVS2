#include "profiling.h"

double Profiling::deviation(std::vector<double> *arrayOfDoubles) {
    double result;
    double sumPower;
    double tmp;
    double div;

    result = average(arrayOfDoubles);
    result = powerOf(result, 2);
    result = multiplication(result, arrayOfDoubles->size());

    for (unsigned int i =0; i < arrayOfDoubles->size(); i++) {
        tmp = arrayOfDoubles->at(i);
        tmp = powerOf(tmp, 2);
        sumPower = addition(sumPower, tmp);
    }

    result = subtraction(sumPower, result);
    div = subtraction(arrayOfDoubles->size(), 1);
    div = division(1, div);
    result = multiplication(div, result);
    result = root(result, 2);
    return result;
};

